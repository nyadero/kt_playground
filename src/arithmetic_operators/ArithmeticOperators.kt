package arithmetic_operators

fun main() {
    var number1: Int = 10
    val number2: Int = 40

//    addition
    println("Additions : ${number1 + number2}")
//    subtraction
    println("Subtraction: ${number2 - number1}")
//    division
    println("Division: ${number2 / number1}")
//    multiplication
    println("Multiplication: ${number1 * number2}")
//    modulus
    println("Modulus: ${number2%number1}")
//    increase number by 1
    println(++number1)
    println(--number1)

}