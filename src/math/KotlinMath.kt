package math

import kotlin.math.*

fun main() {
//    get value of pi
    println("Value of Pi: $PI")
//    print largest number
    println("Largest: ${max(1000, 100)}")
//    print smallest number
    println("Smallest:  ${min(1000, 100)}")
//    square root
    println("Square root: ${sqrt(5.0)}")
//    round off
    println("Round up: ${ceil(5.7)}")
//    round down
    println("Round down: ${floor(5.7)}")
}